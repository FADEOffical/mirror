/**
 * This package contains the classes and interfaces that compose the mirror API. The mirror API is a declarative API
 * that allows the user to use java reflections in a more intuitive way. The API is composed by the following classes:
 * <ul>
 *     <li>{@link fade.mirror.Mirror}: the main class of the API. It is used to create a mirrors of reflection objects.</li>
 *     <li>{@link fade.mirror.MClass}: the class that represents a mirror of a class.</li>
 *     <li>{@link fade.mirror.MField}: the class that represents a mirror of a field.</li>
 *     <li>{@link fade.mirror.MMethod}: the class that represents a mirror of a method.</li>
 *     <li>{@link fade.mirror.MParameter}: the class that represents a mirror of a parameter.</li>
 * </ul>
 */
package fade.mirror;
